#!/bin/bash
# op-toolkit-init.sh -- interactive setup: pick which 1Password vaults to cache
# writes ~/.config/op-toolkit/config.env so other op-toolkit scripts know
# which vaults to bulk-load on first use
#
# usage:
#   op-toolkit-init.sh

set -euo pipefail

CONFIG_DIR="${HOME}/.config/op-toolkit"
CONFIG_FILE="${OP_TOOLKIT_CONFIG:-$CONFIG_DIR/config.env}"

echo "op-toolkit init — pick the 1Password vault(s) Claude should pre-cache."
echo ""

# fetch vault list — may trigger biometric prompt
echo "fetching vault list..." >&2
if ! vault_json=$(op vault list --format=json 2>&1); then
  echo "error: op vault list failed:" >&2
  echo "$vault_json" >&2
  exit 1
fi

# parse vault names into an array (bash 3.2 compat — no mapfile)
VAULT_NAMES=()
while IFS= read -r line; do
  [ -n "$line" ] && VAULT_NAMES+=("$line")
done < <(printf '%s' "$vault_json" | jq -r '.[].name')

if [ ${#VAULT_NAMES[@]} -eq 0 ]; then
  echo "no vaults found, are you signed in to op?" >&2
  exit 1
fi

echo "available vaults:"
for i in "${!VAULT_NAMES[@]}"; do
  printf '  %d. %s\n' "$((i + 1))" "${VAULT_NAMES[$i]}"
done
echo ""

# prompt with up to 3 retries
MAX_ATTEMPTS=3
attempt=0
SELECTED_NAMES=()

while [ ${#SELECTED_NAMES[@]} -eq 0 ]; do
  attempt=$((attempt + 1))
  if [ "$attempt" -gt "$MAX_ATTEMPTS" ]; then
    echo "no valid selection after $MAX_ATTEMPTS attempts, exiting" >&2
    exit 1
  fi

  printf 'enter vault numbers (space- or comma-separated, e.g. `1 3`): '
  read -r raw_input

  if [ -z "$raw_input" ]; then
    echo "no input provided, try again ($attempt/$MAX_ATTEMPTS)"
    continue
  fi

  # normalize: replace commas with spaces, then split
  normalized="${raw_input//,/ }"
  read -ra tokens <<< "$normalized"

  valid=true
  candidate_names=()

  for tok in "${tokens[@]}"; do
    # skip empty tokens from double-spaces etc
    [ -z "$tok" ] && continue

    # validate it's a number
    if ! [[ "$tok" =~ ^[0-9]+$ ]]; then
      echo "invalid input '$tok': must be a number, try again"
      valid=false
      break
    fi

    idx=$((tok - 1))

    if [ "$idx" -lt 0 ] || [ "$idx" -ge "${#VAULT_NAMES[@]}" ]; then
      echo "invalid number $tok: must be between 1 and ${#VAULT_NAMES[@]}, try again"
      valid=false
      break
    fi

    candidate_names+=("${VAULT_NAMES[$idx]}")
  done

  if [ "$valid" = false ] || [ ${#candidate_names[@]} -eq 0 ]; then
    [ ${#candidate_names[@]} -eq 0 ] && echo "no valid numbers found, try again ($attempt/$MAX_ATTEMPTS)"
    continue
  fi

  SELECTED_NAMES=("${candidate_names[@]}")
done

# build display string
selected_display=$(IFS=', '; echo "${SELECTED_NAMES[*]}")
echo ""
echo "selected vaults: $selected_display"

# build space-separated vault string for config
vaults_value=$(IFS=' '; echo "${SELECTED_NAMES[*]}")

# create config dir
mkdir -p "$CONFIG_DIR"
chmod 700 "$CONFIG_DIR"

# write config file
iso_date=$(date -u +"%Y-%m-%dT%H:%M:%SZ")
cat > "$CONFIG_FILE" <<EOF
# generated by op-toolkit-init.sh on $iso_date
OP_TOOLKIT_VAULTS="$vaults_value"
EOF
chmod 600 "$CONFIG_FILE"

echo "wrote $CONFIG_FILE"
echo ""
echo "run \`op-bulk-load.sh\` to pre-cache them now (one biometric prompt per vault), or use \`op-cache-get.sh\` directly — it will load on first use."
